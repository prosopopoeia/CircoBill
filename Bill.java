    private void loadBillCust(){
        String datum[] = nameField.getText().split("\\s+");
        String[] cityState = cszField.getText().split("\\s+");  //Splits city, state & zip into seperate vars
        //split the contents of the name field into a seperate first and last name
        try{
            if(datum.length<2){
                aCustomer.setLastName(datum[0]);
            } else if(datum.length == 2) {
                aCustomer.setFirstName(datum[0]);
                aCustomer.setLastName(datum[1]);
            }else {
                aCustomer.setFirstName(datum[1]);
                aCustomer.setLastName(datum[2]);
            }
               if(!propertyAddyField.getText().equals("") || !propertyAddyField.getText().equals("propertyAddress")){
                aCustomer.setAddy(propertyAddyField.getText());
                aCustomer.setMailAddy(mailField.getText());
            }else{
                aCustomer.setAddy(mailField.getText());
                aCustomer.setMailAddy(mailField.getText());
            }
            // ternary to get rid of trailing commas (,) from state field
            aCustomer.setCity((cityState[0].charAt(cityState[0].length()-1)==',') ? cityState[0] = cityState[0].substring(0, cityState[0].length()-1) : cityState[0]);
            aCustomer.setState(cityState[1]);
            aCustomer.setZip(cityState[2]);
            aCustomer.setPhone(phoneField.getText());
            if(!phoneField2.equals("") && !phoneField2.equals("Alternate Customer Phone Number"))
               aCustomer.setPhone2(phoneField2.getText());
            /*------------------------------------Bill stuff--------------------------------*/
           aBill.setBillDate(billDateField.getText());
           aBill.setCustID(aCustomer.getCustID());
           aBill.setDateOfService(dosField.getText());
           aBill.setInvNum(invoiceField.getText());
           aBill.setTime(timeField.getText());
           aBill.setMisc(miscFees);
           aBill.setServiceReport(serviceArea.getText());
           aBill.setMaterialString(materialArea.getText());
           aBill.setLabor(laborFees);
           aBill.setMaterialsTotal(materialTotal);
           aBill.setTotal(totDue);
           aBill.setTripCharge(tripCharge);
           aBill.setTripFee(true);//???????????????????????????????????
        }catch(NullPointerException e){
            e.printStackTrace();
        }
        db.query(aCustomer, aBill);
      
    }//loadBillCust
    
    private void calcButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcButtonActionPerformed
        int minutes = 0;
        int hours = 0;
        
        invoiceField.setText(calcs.generateID());
        billDateField.setText(calcs.getDate());
        try{
            tripCharge = Double.parseDouble(tripField.getText());
        }catch(NumberFormatException e){
            tripCharge = 0;
        }
        if(!miscField.getText().equals("") && !miscField.getText().equals("-") ){
           miscFees = Double.parseDouble(miscField.getText());
        }else{
            miscFees =  0;
        }
        if(!balanceField.getText().equals("") && !balanceField.getText().equals("-") ){
            prevBalance = Double.parseDouble(balanceField.getText());
        }else{
            prevBalance =  0;
        }
        materialTotal = calcs.totalMaterials(materialArea.getText()); 
        materialField.setText(decf.format(materialTotal));
        if(minutesField.getText() != null && Character.isDigit(minutesField.getText().charAt(0))){
           minutes = !minutesField.equals("minutes") ? Integer.parseInt(minutesField.getText()) : 0;
        }
      /*******************************************************?*/
        if(hoursField.getText() != null && Character.isDigit(hoursField.getText().charAt(0)))
           hours = Integer.parseInt(hoursField.getText());
           
           timeField.setText(hours + " hour(s) " + minutes + " min.");
           if (dosField.getText().equals("Date of Service:"))
               dosField.setText(" ");
           laborFees = calcs.calcLabor(hours, minutes);
           laborChargeField.setText(decf.format(laborFees));
           totDue = (calcs.calcTotal(materialTotal, laborFees, tripCharge, miscFees, prevBalance));
           dueField1.setText(decf.format(totDue));
           dueField2.setText(decf.format(totDue));
           loadBillCust();

            
            
        
    }//GEN-LAST:event_calcButtonActionPerformed

