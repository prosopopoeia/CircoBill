    @Override
    public int print(Graphics g, PageFormat pf, int page) throws
                                                        PrinterException {

        if (page > 0) { 
            return NO_SUCH_PAGE;
        }
        Graphics2D g2d = (Graphics2D)g;
        g2d.translate(pf.getImageableX(), pf.getImageableY());
        if(materialOnly)
            materialArea.printAll(g);
        else
            jPanel1.printAll(g);
        return PAGE_EXISTS;
    }//end print
    
